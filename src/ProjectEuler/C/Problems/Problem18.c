void step_up(int g[][15],int r){
    int *temp = malloc(r * sizeof(int));
    int n;
    for (n = 0;n < r + 1;n++){
	temp[n] = (g[r][n] > g[r][n+1]) ? g[r][n] : g[r][n+1];
    }
    for (n = 0;n < r + 1;n++){
	g[r-1][n] = g[r-1][n] + temp[n];
    }
    free(temp);
}
